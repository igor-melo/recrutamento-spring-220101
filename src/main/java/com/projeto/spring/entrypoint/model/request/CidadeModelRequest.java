package com.projeto.spring.entrypoint.model.request;

public class CidadeModelRequest {

    private String nome;
    private String estado;

    public CidadeModelRequest(String nome, String estado) {
        this.nome = nome;
        this.estado = estado;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
}
